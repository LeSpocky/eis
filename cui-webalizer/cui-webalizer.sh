mkdir -p /etc/webalizer
rm -f /etc/webalizer/webalizer_*.conf

# index no. for all config files 
conf_idx=1

idx=1
while [ "$idx" -le "$WEBALIZER_HOST_N" ]
do
    eval accesslog='$WEBALIZER_HOST_'$idx'_ACCESS_LOG'
    eval output_dir='$WEBALIZER_HOST_'$idx'_OUTPUT_DIR'
    eval host_name='$WEBALIZER_HOST_'$idx'_HOST_NAME'
    eval title='$WEBALIZER_HOST_'$idx'_TITLE'
    eval bgcolor='$WEBALIZER_HOST_'$idx'_BGCOLOR'
    eval type='$WEBALIZER_HOST_'$idx'_TYPE'

    [ ! -f $accesslog ] && echo "WARNING: The file '$accesslog' does not exist!" 

    mkdir -p ${output_dir}
    chmod 0750 ${output_dir}
    chown -R root:apache ${output_dir}

    create_webalizer_configuration > /etc/webalizer/webalizer_${conf_idx}.conf
    idx=$((idx+1))
    conf_idx=$((conf_idx+1))
done


if [ "$WEBALIZER_VHOSTS_RUN_ALL" = 'yes' ] ; then 
    idx=1
    while [ "$idx" -le "$APACHE2_VHOST_N" ]
    do
        eval active='$APACHE2_VHOST_'$idx'_ACTIVE'
        if [ "$active" != "yes" ] ; then
	    idx=$((idx+1))
            continue
        fi
        eval servername='$APACHE2_VHOST_'$idx'_SERVER_NAME'
        eval docroot='$APACHE2_VHOST_'$idx'_DOCUMENT_ROOT'
        eval accesscontrol='$APACHE2_VHOST_'$idx'_ACCESS_CONTROL'
        accesslog="/var/log/apache2/access-${servername}.log"

        # first remove the trailing slash
        doc_root=`echo $docroot | sed -e 's|/$||'`
        case "$WEBALIZER_VHOSTS_OUTPUT_DIR" in
            *VHOST_DOCROOT*)
                output_dir=`echo $WEBALIZER_VHOSTS_OUTPUT_DIR | sed "s|%VHOST_DOCROOT%|${doc_root}|"`
                ;;
            *SERVER_NAME*)
                output_dir=`echo $WEBALIZER_VHOSTS_OUTPUT_DIR | sed "s|%SERVER_NAME%|${servername}|"`
                ;;
            *)
                output_dir="$WEBALIZER_VHOSTS_OUTPUT_DIR"
                ;;        
        esac
        host_name="$servername"
        title="$WEBALIZER_VHOSTS_TITLE"
        bgcolor="$WEBALIZER_VHOSTS_BGCOLOR"
        type='clf'

        [ ! -f $accesslog ] && echo "WARNING: The file '$accesslog' does not exist!"

        mkdir -p ${output_dir}
        chmod 0750 ${output_dir}
        chown -R root:apache ${output_dir}

        create_webalizer_configuration > /etc/webalizer/webalizer_${conf_idx}.conf

	idx=$((idx+1))
	conf_idx=$((conf_idx+1))
    done
fi


if [ "$START_WEBALIZER" = "yes" ] ; then
    mkdir -p /etc/cron/root
    echo "$WEBALIZER_CRON /usr/bin/webalizer-run cron" >/etc/cron/root/webalizer
    rc-service --quiet fcron reload
else
    rm -f /etc/cron/root/webalizer
    rc-service --quiet fcron reload
fi

exit 0
